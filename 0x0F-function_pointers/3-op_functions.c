/**
	* op_add - returns the sum of passed ints
	* @a: num 1
	* @b: num2
	* Return: sum of two nums
	*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
	* op_sub - returns the result of passed ints
	* @a: num 1
	* @b: num2
	* Return: sum of two nums
	*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
	* op_mul - returns the result of passed ints
	* @a: num 1
	* @b: num2
	* Return: sum of two nums
	*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
	* op_div - returns the result of passed ints
	* @a: num 1
	* @b: num2
	* Return: sum of two nums
	*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
	* op_mod - returns the remainder of passed ints
	* @a: num 1
	* @b: num2
	* Return: sum of two nums
	*/
int op_mod(int a, int b)
{
	return (a % b);
}
